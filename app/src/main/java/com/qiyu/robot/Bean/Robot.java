public class Robot {

    private String robotName;

    private String robotContent;

    private String robotSex;

    public String getRobotName() {
        return robotName;
    }

    public void setRobotName(String robotName) {
        this.robotName = robotName;
    }

    public String getRobotContent() {
        return robotContent;
    }

    public void setRobotContent(String robotContent) {
        this.robotContent = robotContent;
    }

    public String getRobotSex() {
        return robotSex;
    }

    public void setRobotSex(String robotSex) {
        this.robotSex = robotSex;
    }

}
